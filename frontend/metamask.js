const init = async () => {
	const web3 = new Web3(ethNode);

	console.log("Connection successful!");

	const wojekContract = new web3.eth.Contract(wojekAbi, wojekAddress);

	mintsLeft.innerHTML = await wojekContract.methods.mintsLeft().call();

	mintCost = await wojekContract.methods.mintCost().call();

	var mintCostETH = parseInt(mintCost) / 10**18;
	mintCostText.innerHTML = mintCostETH.toString() + " ETH";

	var supply = await wojekContract.methods.totalSupply().call();
	let tokenId = parseInt(supply) - 1;

	var tokenUri = await wojekContract.methods.tokenURI(tokenId).call();
	tokenUri = atob(tokenUri.substr(29));
	tokenUri = JSON.parse(tokenUri);

	latestMint.src = tokenUri.image;
	latestId.innerHTML = "Latest mint #" + tokenId.toString();
}

init();

