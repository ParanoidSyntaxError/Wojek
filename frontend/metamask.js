async function transaction(weiAmount) {
	ethereum.request({
		method: 'eth_sendTransaction',
		params: [{
				from: accounts[0],
				to: wojekAddress,
				value: weiAmount.toString(16),
			},
		],
	})
	.then((txHash) => {
		console.log(txHash);
	})
	.catch((error) => {
		console.log(error);
	});
}